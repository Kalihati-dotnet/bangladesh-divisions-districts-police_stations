--
-- Table structure for table `bd_divisions`
--

CREATE TABLE `bd_divisions` (
  `__id` tinyint unsigned NOT NULL PRIMARY KEY AUTO_INCREMENT,
  `en_name` varchar(20) NOT NULL,
  `bn_name` varchar(20) NOT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_unicode_ci;

--
-- Table structure for table `bd_districts`
--

CREATE TABLE `bd_districts` (
  `__id` tinyint unsigned NOT NULL PRIMARY KEY AUTO_INCREMENT,
  `div_id` tinyint unsigned NOT NULL,
  `en_name` varchar(20) NOT NULL,
  `bn_name` varchar(20) NOT NULL,
  `website` varchar(64) NOT NULL,
  FOREIGN KEY (`div_id`) REFERENCES bd_divisions(`__id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_unicode_ci;

--
-- Table structure for table `bd_police_stations`
--

CREATE TABLE `bd_police_stations` (
  `__id` smallint unsigned NOT NULL PRIMARY KEY AUTO_INCREMENT,
  `dis_id` tinyint unsigned NOT NULL,
  `en_name` varchar(30) NOT NULL,
  `bn_name` varchar(30) NOT NULL,
  FOREIGN KEY (`dis_id`) REFERENCES bd_districts(`__id`)
